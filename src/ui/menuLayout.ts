/** Convert a source screen rectangle to coordinates inside M_BlackScreen. */
export function menuBandRectStyle(rect: MenuRect): {
  left: string;
  top: string;
  width: string;
  height: string;
  position: 'absolute';
} {
  const bandWidth = MENU_BAND_RECT[2] - MENU_BAND_RECT[0];
  return {
    position: 'absolute',
    left: `${((rect[0] - MENU_BAND_RECT[0]) / bandWidth) * 100}%`,
    top: `${rect[1] * 100}%`,
    width: `${((rect[2] - rect[0]) / bandWidth) * 100}%`,
    height: `${(rect[3] - rect[1]) * 100}%`,
  };
}
